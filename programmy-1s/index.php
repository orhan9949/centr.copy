        $(document).ready(function(){
            $('.check_bl').click(function(){
                if($('.check_bl:checked').length > 0)
                {
                    $('.bl_filter').addClass('element_filter_folse');
                    if($(this).prop('checked')) {

                        $('.bl_'+$(this).attr('filter')).addClass('element_filter_true');
                    }
                    else {
                        $('.bl_'+$(this).attr('filter')).removeClass('element_filter_true')
                    }
                }else {
                    $('.bl_filter').removeClass('element_filter_folse');
                    $('.bl_filter').removeClass('element_filter_true');
                }
            });
